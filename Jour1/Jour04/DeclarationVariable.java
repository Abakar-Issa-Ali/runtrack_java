public class DeclarationVariable {

    public static void main(String[] args) { 

        int a; int b; int c ;  // Déclaration de trois variables de type int sur cette 
        a = 0; b = 0; c = 0;  // Initialisation de trois variables de type int à la valeur 0
        
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}
